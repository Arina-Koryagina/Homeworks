print("\n\t\tIV")
# Даны числа x, y. Проверить истинность высказывания:
# «Точка с координатами (x, y) лежит во второй или
# третьей координатной четверти».

x = float(input("Введіть значення координати x: "))
y = float(input("Введіть значення координати y: "))
res = x < 0 and (y > 0 or y < 0)
print(res)
