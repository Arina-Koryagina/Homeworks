print("\t\tI")
# Даны числа x, y, x1, y1, x2, y2. Проверить истинность высказывания:
# «Точка с координатами (x, y) лежит внутри прямоугольника, левая верхняя
# вершина которого имеет координаты (x1, y1), правая нижняя — (x2, y2), а
# стороны параллельны координатным осям».

x = float(input("Введіть значення координати x: ")) # 0, 2, 4
y = float(input("Введіть значення координати y: ")) # 2, 1, 3
x1 = float(input("Введіть значення координати x1: ")) # -1
y1 = float(input("Введіть значення координати y1: ")) # 3
x2 = float(input("Введіть значення координати x2: ")) # 4
y2 = float(input("Введіть значення координати y2: ")) # -1
res = (x1 > x > x2 or x2 > x > x1) and (y1 > y > y2 or y2 > y > y1)
print(res)

#  II
print("\n\t\tII")
# Дано трехзначное число. Проверить истинность высказывания:
# «Цифры данного числа образуют возрастающую или убывающую последовательность».

n = int(input("Введіть трьохзначне число: "))
n1 = n // 100
n2 = n // 10 % 10
n3 = n % 10
res = n1 < n2 < n3 or n1 > n2 > n3
print(res)

