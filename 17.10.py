# Даны числа A, B, C (число А не равно 0). Рассмотрев дискриминант
# D = B*B - 4*A*C, проверить истинность высказывания:
# «Квадратное уравнение Ax^2 + Bx + C = 0 имеет вещественные корни».

A = float(input("Введіть значення змінної А: ")) # 2, 1
B = float(input("Введіть значення змінної В: ")) # 4, 1
C = float(input("Введіть значення змінної С: ")) # 2, -2
D = B**2 - 4*A*C
res = not (D < 0)
print(res)

